// 8. for...of Statement

// Definition:
// Iterates over the values of an iterable object (like arrays, strings, maps, sets).

// Syntax

// for(let value of iterable)
// {
//     //code block
// }

let fruits = ["apple" ,  "banana" , "mango"];
for(let fruit of fruits)
{
    console.log(fruit);
    
}